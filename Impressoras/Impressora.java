public class Impressora {

    public void exibir(double a){
        System.out.println(a);
    }

    public void exibir(double b, double c){
        System.out.println(b + "," + c);
    }

    public void exibir(float x, String s){
        System.out.println(x + "," + s);
    }

    public void exibir(String o, float z){
        System.out.println(o + "," + z);
    }

    public void exibir(String d, String e, String i){
        System.out.println(d + "," + e + "," + i);
    }
}
